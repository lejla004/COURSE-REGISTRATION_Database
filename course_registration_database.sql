
-- COURSE REGISTRATION DATABASE SYSTEM


CREATE DATABASE IF NOT EXISTS CourseRegistrationDB;
USE CourseRegistrationDB;


-- TABLE: Faculty

CREATE TABLE Faculty (
    faculty_id   INT PRIMARY KEY,
    name         VARCHAR(255) NOT NULL,
    dean         VARCHAR(255)
);


-- TABLE: Department

CREATE TABLE Department (
    dep_id              INT PRIMARY KEY AUTO_INCREMENT,
    name                VARCHAR(255) NOT NULL,
    faculty_id          INT NOT NULL,
    program_coordinator VARCHAR(200) NULL,
    CONSTRAINT fk_dept_faculty FOREIGN KEY (faculty_id) REFERENCES Faculty(faculty_id)
);


CREATE TABLE Professor (
    prof_id    INT PRIMARY KEY AUTO_INCREMENT,
    name       VARCHAR(200) NOT NULL,
    last_name  VARCHAR(200) NOT NULL,
    email      VARCHAR(255),
    dep_id     INT,
    faculty_id INT,
    CONSTRAINT fk_prof_dept    FOREIGN KEY (dep_id)     REFERENCES Department(dep_id),
    CONSTRAINT fk_prof_faculty FOREIGN KEY (faculty_id) REFERENCES Faculty(faculty_id)
);


-- TABLE: Course

CREATE TABLE Course (
    course_id        INT PRIMARY KEY AUTO_INCREMENT,
    c_code           VARCHAR(10)  NOT NULL UNIQUE,
    course_name      VARCHAR(100) NOT NULL,
    credits          INT          NOT NULL,
    dep_id           INT          NOT NULL,
    semester_offered VARCHAR(20),
    year             INT,
    quota            INT,
    prof_id          INT,
    CONSTRAINT fk_course_dept    FOREIGN KEY (dep_id)   REFERENCES Department(dep_id),
    CONSTRAINT fk_course_prof    FOREIGN KEY (prof_id)  REFERENCES Professor(prof_id)
);


-- TABLE: Student
-- (stud_id is an 8-digit code generated by trigger)

CREATE TABLE Student (
    stud_id          VARCHAR(8)   PRIMARY KEY,
    name             VARCHAR(100) NOT NULL,
    last_name        VARCHAR(100) NOT NULL,
    email            VARCHAR(255) NOT NULL,
    dob              DATE         NOT NULL,   -- date of birth
    dor              DATE         NOT NULL,   -- date of registration
    current_credits  INT          DEFAULT 0,
    gpa              DECIMAL(3,2) DEFAULT 0.00,
    faculty          INT          NOT NULL,
    dep_id           INT          NOT NULL,
    CONSTRAINT fk_student_faculty FOREIGN KEY (faculty) REFERENCES Faculty(faculty_id),
    CONSTRAINT fk_student_dept    FOREIGN KEY (dep_id)  REFERENCES Department(dep_id)
);


-- TABLE: Prerequisite

CREATE TABLE Prerequisite (
    course_id       VARCHAR(10) NOT NULL,
    prerequisite_id VARCHAR(10) NOT NULL,
    c_code          VARCHAR(10),
    PRIMARY KEY (course_id, prerequisite_id),
    CONSTRAINT fk_prereq_course FOREIGN KEY (c_code) REFERENCES Course(c_code)
);


-- TABLE: Enrollment

CREATE TABLE Enrollment (
    enrollment_id INT PRIMARY KEY AUTO_INCREMENT,
    student_id    VARCHAR(8)   NOT NULL,
    course_code   VARCHAR(10)  NOT NULL,
    prof_id       INT,
    status        VARCHAR(20)  DEFAULT 'In Progress',
    grade         CHAR(1),
    grade_point   DECIMAL(3,2),
    year          INT,
    CONSTRAINT fk_enroll_student FOREIGN KEY (student_id)  REFERENCES Student(stud_id),
    CONSTRAINT fk_enroll_course  FOREIGN KEY (course_code) REFERENCES Course(c_code),
    CONSTRAINT fk_enroll_prof    FOREIGN KEY (prof_id)     REFERENCES Professor(prof_id)
);


-- TRIGGER 1: auto_generate_stud_id
-- Generates 8-digit student ID before INSERT on Student
-- Format: [2-digit year][2-digit faculty_id][2-digit dep_id][2-digit increment]

DELIMITER $$

CREATE TRIGGER auto_generate_stud_id
BEFORE INSERT ON Student
FOR EACH ROW
BEGIN
    DECLARE next_increment  INT;
    DECLARE enrollment_year VARCHAR(2);
    DECLARE v_faculty_id    VARCHAR(2);
    DECLARE v_department_id VARCHAR(2);
    DECLARE student_base_id VARCHAR(6);

    -- Extract last 2 digits of the registration year
    SET enrollment_year = RIGHT(CAST(YEAR(NEW.dor) AS CHAR), 2);

    -- Pad faculty_id to 2 digits
    SELECT LPAD(faculty_id, 2, '0') INTO v_faculty_id
    FROM Faculty
    WHERE faculty_id = NEW.faculty;

    -- Pad dep_id to 2 digits
    SELECT LPAD(dep_id, 2, '0') INTO v_department_id
    FROM Department
    WHERE dep_id = NEW.dep_id;

    -- Build the first 6 digits
    SET student_base_id = CONCAT(enrollment_year, v_faculty_id, v_department_id);

    -- Get the next auto-increment suffix (last 2 digits)
    SELECT COALESCE(MAX(CAST(SUBSTRING(stud_id, 7, 2) AS UNSIGNED)), 0) + 1
    INTO next_increment
    FROM Student
    WHERE faculty = NEW.faculty
      AND dep_id  = NEW.dep_id
      AND RIGHT(CAST(YEAR(dor) AS CHAR), 2) = enrollment_year;

    -- Assign the generated student ID
    SET NEW.stud_id = CONCAT(student_base_id, LPAD(next_increment, 2, '0'));
END$$


-- TRIGGER 2: CheckPrerequisite
-- Prevents enrollment if student previously failed the prerequisite

CDELIMITER $$

CREATE TRIGGER CheckPrerequisite
BEFORE INSERT ON Enrollment
FOR EACH ROW
BEGIN
    DECLARE prerequisite_failed BOOLEAN DEFAULT FALSE;
    DECLARE prereq_count INT;

    IF EXISTS (SELECT 1 FROM Prerequisite WHERE course_id = NEW.course_code) THEN

        SELECT COUNT(*)
        INTO prereq_count
        FROM Prerequisite p
        JOIN Enrollment e ON e.course_code = p.prerequisite_id
        WHERE e.student_id = NEW.student_id
        AND p.course_id = NEW.course_code
        AND e.grade = 'F';

        IF prereq_count > 0 THEN
            SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Cannot enroll due to failing prerequisite';
        END IF;

    END IF;

END$$

DELIMITER ;

DELIMITER $$

CREATE TRIGGER SetGrade
BEFORE INSERT ON Enrollment
FOR EACH ROW
BEGIN
    IF NEW.grade_point IS NOT NULL THEN

        SET NEW.grade =
            CASE
                WHEN NEW.grade_point >= 90 THEN 'A'
                WHEN NEW.grade_point >= 80 THEN 'B'
                WHEN NEW.grade_point >= 70 THEN 'C'
                WHEN NEW.grade_point >= 60 THEN 'D'
                ELSE 'F'
            END;

        SET NEW.status =
            CASE
                WHEN NEW.grade_point < 60 THEN 'Failed'
                ELSE 'Completed'
            END;

    END IF;
END$$

DELIMITER ;


DELIMITER $$

CREATE TRIGGER UpdateGPA
AFTER INSERT ON Enrollment
FOR EACH ROW
BEGIN
    DECLARE total_quality_points DECIMAL(10,2);
    DECLARE total_credits INT;
    DECLARE calculated_gpa DECIMAL(3,2);

    SELECT 
        SUM(c.credits *
            CASE
                WHEN e.grade = 'A' THEN 4
                WHEN e.grade = 'B' THEN 3
                WHEN e.grade = 'C' THEN 2
                WHEN e.grade = 'D' THEN 1
                WHEN e.grade = 'F' THEN 0
            END),
        SUM(c.credits)
    INTO total_quality_points, total_credits
    FROM Enrollment e
    JOIN Course c ON c.c_code = e.course_code
    WHERE e.student_id = NEW.student_id
      AND e.grade IS NOT NULL;

    IF total_credits > 0 THEN
        SET calculated_gpa = total_quality_points / total_credits;
    ELSE
        SET calculated_gpa = 0;
    END IF;

    UPDATE Student
    SET gpa = calculated_gpa
    WHERE stud_id = NEW.student_id;

END$$

DELIMITER ;

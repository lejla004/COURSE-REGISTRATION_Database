-- Q1: Find students enrolled in a specific course (by course code)
SELECT
    s.stud_id,
    CONCAT(s.name, ' ', s.last_name) AS student_name,
    c.course_name
FROM Student s
JOIN Enrollment e ON s.stud_id   = e.student_id
JOIN Course     c ON e.course_code = c.c_code
WHERE c.c_code = 'SE101';

-- Q2: Find students enrolled in a course WITH grade and status
SELECT
    s.stud_id,
    CONCAT(s.name, ' ', s.last_name) AS student_name,
    c.course_name,
    e.grade,
    e.status
FROM Student s
JOIN Enrollment e ON s.stud_id    = e.student_id
JOIN Course     c ON e.course_code = c.c_code
WHERE c.c_code = 'CS101';

-- Q3: Count total courses a specific student is enrolled in
SELECT
    e.student_id,
    COUNT(e.course_code) AS total_courses
FROM Enrollment e
WHERE e.student_id = '23100101'
GROUP BY e.student_id;

-- Q4: Student enrollment table (name, course, grade, grade_point)
SELECT
    s.name           AS student_name,
    s.last_name      AS student_lastname,
    e.grade,
    e.grade_point,
    c.course_name    AS course_title
FROM Enrollment e
JOIN Student s ON e.student_id  = s.stud_id
JOIN Course  c ON e.course_code = c.c_code
WHERE e.student_id = '23100101';

-- Q5: Find courses taught by a specific professor (no duplicates)
SELECT DISTINCT
    p.name        AS prof_name,
    p.last_name   AS prof_lastname,
    c.course_name,
    c.dep_id
FROM Professor p
JOIN Enrollment e ON p.prof_id    = e.prof_id
JOIN Course     c ON e.course_code = c.c_code
WHERE p.prof_id = 1;

